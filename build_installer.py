"""Build a Windows installer for DCS Command Palette using Inno Setup.

Usage:
    python build_installer.py

Steps:
1. Runs PyInstaller to create dist/dcs-command-palette/
2. Generates an Inno Setup .iss script
3. Runs Inno Setup compiler (ISCC.exe) to produce the installer .exe

Requirements:
    - PyInstaller: pip install pyinstaller
    - Inno Setup 6: https://jrsoftware.org/isdl.php
      (default install path, or set ISCC_PATH env var)

Produces:
    dist/DCS-Command-Palette-v{version}-Setup.exe
"""
from __future__ import annotations

import os
import subprocess
import sys

PROJECT_DIR = os.path.dirname(os.path.abspath(__file__))
DIST_DIR = os.path.join(PROJECT_DIR, "dist", "dcs-command-palette")
ISS_PATH = os.path.join(PROJECT_DIR, "dist", "installer.iss")

# Common Inno Setup locations
_ISCC_CANDIDATES = [
    os.environ.get("ISCC_PATH", ""),
    os.path.join(os.environ.get("LOCALAPPDATA", ""), "Programs", "Inno Setup 6", "ISCC.exe"),
    r"C:\Program Files (x86)\Inno Setup 6\ISCC.exe",
    r"C:\Program Files\Inno Setup 6\ISCC.exe",
]


def _find_iscc() -> str | None:
    """Find the Inno Setup compiler (ISCC.exe)."""
    for path in _ISCC_CANDIDATES:
        if path and os.path.isfile(path):
            return path
    return None


def _get_version() -> str:
    """Read version from pyproject.toml."""
    try:
        toml_path = os.path.join(PROJECT_DIR, "pyproject.toml")
        with open(toml_path, encoding="utf-8") as f:
            for line in f:
                if line.strip().startswith("version"):
                    return line.split("=", 1)[1].strip().strip('"').strip("'")
    except (OSError, IndexError):
        pass
    return "0.0.0"


