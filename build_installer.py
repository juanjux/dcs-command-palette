def build_pyinstaller() -> bool:
    """Run PyInstaller to create the dist folder."""
    print("=" * 60)
    print("  Step 1/2: Building .exe with PyInstaller")
    print("=" * 60)
    print()

    cmd = [
        sys.executable, "-m", "PyInstaller",
        "--name", "dcs-command-palette",
        "--noconsole",
        "--noconfirm",
        "--add-data", os.path.join("src", "lua", "dcs_command_palette_hook.lua") + f"{os.pathsep}.",
        "--hidden-import", "src.installer.wizard",
        "--hidden-import", "src.bios.installer",
        "--hidden-import", "pynput.keyboard._win32",
        "--hidden-import", "pynput.mouse._win32",
        "--collect-all", "pygame",
        "main.py",
    ]

    result = subprocess.run(cmd, cwd=PROJECT_DIR)
    if result.returncode != 0:
        print("ERROR: PyInstaller build failed!")
        return False

    print("\nPyInstaller build complete.")
    return True


def build_installer() -> bool:
    """Generate and compile the Inno Setup installer."""
    print()
    print("=" * 60)
    print("  Step 2/2: Creating installer with Inno Setup")
    print("=" * 60)
    print()

    iscc = _find_iscc()
    if not iscc:
        print("ERROR: Inno Setup compiler (ISCC.exe) not found!")
        print("Install Inno Setup 6 from https://jrsoftware.org/isdl.php")
        print("Or set the ISCC_PATH environment variable to the ISCC.exe path.")
        return False

    version = _get_version()
    print(f"Version: {version}")
    print(f"ISCC: {iscc}")

    # Generate the .iss file
    iss_content = _generate_iss(version)
    with open(ISS_PATH, "w", encoding="utf-8") as f:
        f.write(iss_content)
    print(f"Generated: {ISS_PATH}")

    # Compile
    result = subprocess.run([iscc, ISS_PATH], cwd=PROJECT_DIR)
    if result.returncode != 0:
        print("ERROR: Inno Setup compilation failed!")
        return False

    output = os.path.join(PROJECT_DIR, "dist", f"DCS-Command-Palette-v{version}-Setup.exe")
    if os.path.isfile(output):
        size_mb = os.path.getsize(output) / (1024 * 1024)
        print(f"\nInstaller created: {output}")
        print(f"Size: {size_mb:.1f} MB")
    else:
        print(f"\nWARNING: Expected output not found at {output}")

    return True


def main() -> None:
    if not build_pyinstaller():
        sys.exit(1)

    if not build_installer():
        sys.exit(1)

    print()
    print("=" * 60)
    print("  Build complete!")
    print("=" * 60)
    version = _get_version()
    print(f"\n  Installer: dist/DCS-Command-Palette-v{version}-Setup.exe")
    print(f"  ZIP dist:  dist/dcs-command-palette/")
    print()
    print("  Upload the installer .exe to GitHub releases.")
    print()


if __name__ == "__main__":
    main()
